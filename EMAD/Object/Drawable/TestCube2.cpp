void TestCube2::draw(std::shared_ptr<Program> program) noexcept
{
    program->activate();
    // 绑定顶点着色器的常量缓存
    // bind model trans
    program->setMatrix("model", genModelTrans());

    glBindVertexArray(mVAO);

    glDrawArrays(GL_TRIANGLES, 0, 36);
}
